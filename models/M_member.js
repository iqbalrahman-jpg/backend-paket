import mongoose from "mongoose";
import { INTEGER } from "sequelize";
 
const Member = mongoose.Schema({
    nama_lengkap:{
        type: String,
        required: true
    },
    username:{
        type: String,
        required: true
    },
    password:{
        type: String,
        required: true
    },
    email:{
        type: String,
        required: true
    },
    nomor:{
        type: String,
        required: true
    },
    status:{
        type: Number,
        required: true
    },
});
 
// export model
export default mongoose.model('M_member', Member);